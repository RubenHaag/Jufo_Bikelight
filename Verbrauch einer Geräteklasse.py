class Verbrauch_einer_Geräteklasse:
    def __init__(self, geräteanzahl, verbrauch, gesamtverbrauch, zeit):
        self.geräteanzahl = geräteanzahl
        self.verbrach = verbrauch
        self.gesamtverbrauch = gesamtverbrauch
        self.zeit = zeit

    def alles():
        self.gesamtverbrauch = self.geräteanzahl * self.verbrauch * self.zeit
